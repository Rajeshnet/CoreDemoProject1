            {
                Token = RandomTokenString(),
                Expires = DateTime.UtcNow.AddDays(Convert.ToInt32(_configuration["JWTSettings:RefreshTokenValidityInDays"])),
                Created = DateTime.UtcNow,
                CreatedByIp = ipAddress
            };
        }

